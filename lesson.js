// if/else statement

let number = 10;

// Operators:
// = erklærer en verdi
// == sjekker innhold
// === sjekker innhold og datatype
// < mindre enn / less then
// > større enn / more then
// & og / and
// | eller / either 

if (number === 10) {
    console.log("Dette tallet er akkurat 10");
} 
else if (number < 10) {
    console.log("Dette tallet er mindre enn 10")
}
else if (number > 10) {
    console.log("Dette tallet er større enn 10")
}
else {
    console.log("Dette er ikke et tall")
}